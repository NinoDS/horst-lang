#[derive(Copy, Clone, Debug, PartialEq)]
pub enum Instruction {
    Constant(usize),
    True,
    False,
    Nil,
    Pop,
    And,
    Or,
    Add,
    Subtract,
    Multiply,
    Divide,
    Negate,
    Not,
    Equal,
    NotEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,
    Jump(usize),
    JumpBack(usize),
    JumpIfFalse(usize),
    Return,
    Call(usize),
    DefineGlobal(usize),
    GetGlobal(usize),
    SetGlobal(usize),
    GetLocal(usize),
    SetLocal(usize),
    MakeClosure,
    MakeUpvalue(usize, usize),
    GetUpvalue(usize),
    SetUpvalue(usize),
    GetProperty(usize),
    SetProperty(usize),
    GetSuper(usize),
    Print,
    Halt,
    Inherit,
    Invoke(usize),
}